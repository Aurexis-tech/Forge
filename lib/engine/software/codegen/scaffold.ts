// Aurexis Forge — Phase 3 (Software) deterministic scaffold.
//
// The Next.js + Supabase project skeleton the software codegen
// pipeline materialises BEFORE any LLM call. Every file here is
// data — the codegen layer NEVER executes it (same security boundary
// as Phases 1 + 2).
//
// THREE STRUCTURAL NON-NEGOTIABLES live in this file:
//
//   1. NO HAND-ROLLED AUTH — the sign-in page, the auth callback,
//      the session middleware, and the supabase client wiring all
//      live HERE, not in any LLM-filled slot. The slot-kind dispatch
//      in slots.ts routes every auth slot back to these template
//      files; no LLM call ever produces an auth alternative.
//
//   2. SERVER/CLIENT BOUNDARY — `lib/supabase/server.ts` is
//      server-only and uses the cookie-based session client from
//      @supabase/ssr; `lib/supabase/browser.ts` is client-only and
//      uses ONLY the anon key. Neither references SUPABASE_SERVICE_
//      ROLE_KEY. LLM-filled slots see only these two clients in
//      their prompt context — there is no code path for the LLM to
//      reach for the service-role key.
//
//   3. RLS — the per-entity migration is emitted by migration.ts
//      (RLS policies always on); the scaffold's `supabase/migrations`
//      directory carries only template-wide bootstrap, not entity
//      schema. The codegen assembler combines both.

