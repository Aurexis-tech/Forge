// ---------------------------------------------------------------------------
// lib/supabase/server.ts — server-only Supabase client. Uses the
// cookie-based session client from @supabase/ssr so RLS policies
// scope every query to the authenticated user. NEVER touches the
// service-role key.
// ---------------------------------------------------------------------------

const LIB_SUPABASE_SERVER_TS = `// SERVER-ONLY Supabase client. Reads NEXT_PUBLIC_SUPABASE_URL +
// NEXT_PUBLIC_SUPABASE_ANON_KEY and binds them to the request cookie
// store so every query is scoped to the authenticated user under RLS.
//
// This file MUST NOT be imported into a client module. The
// service-role key is NEVER imported here — even though it would
// bypass RLS, it has no use-case in user-facing pages or routes.

import { createServerClient as createSupabaseServer } from '@supabase/ssr';
import { cookies } from 'next/headers';

export function createServerClient() {
  const cookieStore = cookies();
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL ?? '';
  const anonKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY ?? '';
  if (!url || !anonKey) {
    throw new Error(
      'Missing NEXT_PUBLIC_SUPABASE_URL or NEXT_PUBLIC_SUPABASE_ANON_KEY'
    );
  }
  return createSupabaseServer(url, anonKey, {
    cookies: {
      get(name: string) {
        return cookieStore.get(name)?.value;
      },
      set(name: string, value: string, options) {
        try {
          cookieStore.set({ name, value, ...options });
        } catch {
          // Server Components cannot set cookies; the middleware refreshes
          // the session, so a failure here is non-fatal for read paths.
        }
      },
      remove(name: string, options) {
        try {
          cookieStore.set({ name, value: '', ...options });
        } catch {
          // see above
        }
      },
    },
  });
}
`;

