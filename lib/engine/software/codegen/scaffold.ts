const LIB_AUTH_ROLES_TS = `// Aurexis Forge — role gate (template-emitted).
//
// Reads the current user's role from the JWT's app_metadata.role
// claim and returns true when it's in the allowed set. Generated
// pages call this on the server BEFORE rendering protected content.

import { createServerClient } from '@/lib/supabase/server';

export async function userHasAnyRole(allowed: string[]): Promise<boolean> {
  if (allowed.length === 0) return true;
  const supabase = createServerClient();
  const { data } = await supabase.auth.getUser();
  const role = (data.user?.app_metadata?.role as string | undefined) ?? null;
  return role !== null && allowed.includes(role);
}

export async function currentUserId(): Promise<string | null> {
  const supabase = createServerClient();
  const { data } = await supabase.auth.getUser();
  return data.user?.id ?? null;
}
`;

