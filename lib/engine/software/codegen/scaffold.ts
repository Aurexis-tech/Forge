function libAuthRlsTs(entityNames: ReadonlyArray<string>, perUserIsolation: boolean): string {
  return [
    "// Aurexis Forge — per-user isolation declaration (template-emitted).",
    "//",
    "// This file documents the entities whose tables carry per-user RLS",
    "// policies. The actual enforcement lives in the schema migration",
    "// (supabase/migrations/0001_init.sql) — this file is a build-time",
    "// artefact so reviewers can grep for the contract in one place.",
    "",
    'export const PER_USER_ISOLATION = ' + String(perUserIsolation) + ';',
    "",
    'export const ISOLATED_ENTITIES: readonly string[] = [' +
      entityNames.map((n) => JSON.stringify(n)).join(', ') +
      '] as const;',
    "",
  ].join('\n');
}

