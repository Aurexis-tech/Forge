const APP_SIGN_IN_PAGE_TSX = `'use client';

// Aurexis Forge — Supabase Auth sign-in page (template-emitted).
// Uses the BROWSER Supabase client and the standard magic-link flow.
// The codegen pipeline NEVER lets an LLM rewrite this file.

import { useState, type FormEvent } from 'react';
import { createBrowserClient } from '@/lib/supabase/browser';

export default function SignInPage() {
  const [email, setEmail] = useState('');
  const [status, setStatus] = useState<'idle' | 'sending' | 'sent' | 'error'>(
    'idle',
  );
  const [error, setError] = useState<string | null>(null);

  async function onSubmit(e: FormEvent<HTMLFormElement>) {
    e.preventDefault();
    setStatus('sending');
    setError(null);
    const supabase = createBrowserClient();
    const { error: err } = await supabase.auth.signInWithOtp({
      email: email.trim(),
      options: {
        emailRedirectTo:
          (typeof window !== 'undefined' ? window.location.origin : '') +
          '/auth/callback',
      },
    });
    if (err) {
      setStatus('error');
      setError(err.message);
      return;
    }
    setStatus('sent');
  }

  return (
    <main style={{ maxWidth: 420, margin: '4rem auto', padding: '1rem' }}>
      <h1>Sign in</h1>
      <p>We&apos;ll email you a magic link.</p>
      <form onSubmit={onSubmit}>
        <input
          type="email"
          required
          value={email}
          onChange={(e) => setEmail(e.target.value)}
          placeholder="you@example.com"
          style={{ width: '100%', padding: '0.5rem', marginTop: '1rem' }}
        />
        <button
          type="submit"
          disabled={status === 'sending' || status === 'sent'}
          style={{ marginTop: '1rem', padding: '0.5rem 1rem' }}
        >
          {status === 'sending' ? 'Sending…' : status === 'sent' ? 'Check your inbox' : 'Send magic link'}
        </button>
      </form>
      {error ? <p style={{ color: 'crimson', marginTop: '1rem' }}>{error}</p> : null}
    </main>
  );
}
`;

